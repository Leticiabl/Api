package ftt.ec.hw.entidade;

public class Notas {

	private int id;
	private String nome;
	private double n1_1;
	private double n2_1;
	private double n1_2;
	private double n2_2;
	private double forma;
	private double nota_final;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public double getN1_1() {
		return n1_1;
	}
	public void setN1_1(double n1_1) {
		this.n1_1 = n1_1;
	}
	public double getN2_1() {
		return n2_1;
	}
	public void setN2_1(double n2_1) {
		this.n2_1 = n2_1;
	}
	public double getN1_2() {
		return n1_2;
	}
	public void setN1_2(double n1_2) {
		this.n1_2 = n1_2;
	}
	public double getN2_2() {
		return n2_2;
	}
	public void setN2_2(double n2_2) {
		this.n2_2 = n2_2;
	}
	public double getForma() {
		return forma;
	}
	public void setForma(double forma) {
		this.forma = forma;
	}
	public double getNota_final() {
		return nota_final;
	}
	public void setNota_final(double nota_final) {
		this.nota_final = nota_final;
	}

}
